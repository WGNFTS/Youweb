public class GerenciamentoConteudo {
    private static List<String> videos = new ArrayList<>();

    public static void adicionarVideo(String video) {
        // Simulação de adição de vídeo
        videos.add(video);
    }

    public static List<String> obterVideos() {
        // Simulação de obtenção da lista de vídeos
        return videos;
    }
}

